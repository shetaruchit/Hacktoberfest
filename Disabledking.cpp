#include <bits/stdc++.h>
using namespace std;
#define ll long long
//<------------------------------------------------------------------------------------------------------------------------------------------>
//<------------------------------------------------------------------------------------------------------------------------------------------>
int main()
{
    ll t = 1;
    cin >> t;
    while (t--)
    {
        int n;
        cin >> n;
        if (n % 2 == 0)
        {
            cout << n << endl;
        }
        else
        {
            cout << n - 1 << endl;
        }
    }
    return 0;
}
